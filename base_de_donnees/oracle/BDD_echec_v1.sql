-- ============================================================
-- 2) COMPTES, PROFILS, CONSENTEMENTS
-- ============================================================

CREATE TABLE compte_membre (
    identifiant_compte_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    adresse_courriel VARCHAR2(320 CHAR) NOT NULL,
    mot_de_passe_hache VARCHAR2(255 CHAR) NOT NULL,
    courriel_verifie_le TIMESTAMP WITH TIME ZONE NULL,
    identifiant_role_compte NUMBER NOT NULL,
    identifiant_statut_compte NUMBER NOT NULL,
    derniere_connexion_le TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_compte_membre_role
        FOREIGN KEY (identifiant_role_compte)
        REFERENCES role_compte (identifiant_role_compte),
    CONSTRAINT fk_compte_membre_statut
        FOREIGN KEY (identifiant_statut_compte)
        REFERENCES statut_compte (identifiant_statut_compte)
);

CREATE UNIQUE INDEX uq_compte_membre_email_lower
    ON compte_membre (LOWER(adresse_courriel));

CREATE TABLE profil_membre (
    identifiant_profil_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NOT NULL,
    prenom VARCHAR2(100 CHAR) NOT NULL,
    nom VARCHAR2(100 CHAR) NOT NULL,
    date_naissance DATE NULL,
    biographie CLOB NULL,
    pseudo_chess_com VARCHAR2(50 CHAR) NULL,
    fide_elo NUMBER(5) NULL,
    rapid_elo NUMBER(5) NULL,
    blitz_elo NUMBER(5) NULL,
    bullet_elo NUMBER(5) NULL,
    accepte_affichage_public_profil CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_profil_membre_compte UNIQUE (identifiant_compte_membre),
    CONSTRAINT ck_profil_public CHECK (accepte_affichage_public_profil IN ('Y', 'N')),
    CONSTRAINT ck_elo_fide CHECK (fide_elo IS NULL OR fide_elo BETWEEN 100 AND 3500),
    CONSTRAINT ck_elo_rapid CHECK (rapid_elo IS NULL OR rapid_elo BETWEEN 100 AND 3500),
    CONSTRAINT ck_elo_blitz CHECK (blitz_elo IS NULL OR blitz_elo BETWEEN 100 AND 3500),
    CONSTRAINT ck_elo_bullet CHECK (bullet_elo IS NULL OR bullet_elo BETWEEN 100 AND 3500),
    CONSTRAINT fk_profil_membre_compte
        FOREIGN KEY (identifiant_compte_membre)
        REFERENCES compte_membre (identifiant_compte_membre)
);

CREATE TABLE consentement_cookie_visiteur (
    identifiant_consentement_cookie_visiteur NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    jeton_consentement VARCHAR2(128 CHAR) NOT NULL,
    version_consentement VARCHAR2(40 CHAR) NOT NULL,
    accepte_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    choix_theme VARCHAR2(10 CHAR) NULL,
    adresse_ip VARCHAR2(64 CHAR) NULL,
    agent_utilisateur VARCHAR2(400 CHAR) NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_cookie_visiteur_jeton UNIQUE (jeton_consentement)
);

CREATE TABLE consentement_membre (
    identifiant_consentement_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NOT NULL,
    identifiant_type_consentement NUMBER NOT NULL,
    version_consentement VARCHAR2(40 CHAR) NOT NULL,
    accepte_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    adresse_ip VARCHAR2(64 CHAR) NULL,
    agent_utilisateur VARCHAR2(400 CHAR) NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_consentement_membre
        UNIQUE (identifiant_compte_membre, identifiant_type_consentement, version_consentement),
    CONSTRAINT fk_consentement_membre_compte
        FOREIGN KEY (identifiant_compte_membre)
        REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_consentement_membre_type
        FOREIGN KEY (identifiant_type_consentement)
        REFERENCES type_consentement (identifiant_type_consentement)
);

CREATE TABLE journal_connexion (
    identifiant_journal_connexion NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NOT NULL,
    connecte_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    adresse_ip VARCHAR2(64 CHAR) NULL,
    agent_utilisateur VARCHAR2(400 CHAR) NULL,
    succes CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    motif_echec VARCHAR2(200 CHAR) NULL,
    CONSTRAINT ck_journal_connexion_succes CHECK (succes IN ('Y', 'N')),
    CONSTRAINT fk_journal_connexion_compte
        FOREIGN KEY (identifiant_compte_membre)
        REFERENCES compte_membre (identifiant_compte_membre)
);

-- ============================================================
-- 3) CONTENU EDITORIAL
-- ============================================================

CREATE TABLE article (
    identifiant_article NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_auteur NUMBER NOT NULL,
    identifiant_statut_publication NUMBER NOT NULL,
    titre VARCHAR2(200 CHAR) NOT NULL,
    slug VARCHAR2(220 CHAR) NOT NULL,
    resume VARCHAR2(500 CHAR) NOT NULL,
    contenu CLOB NOT NULL,
    soumis_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    publie_le TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_article_slug UNIQUE (slug),
    CONSTRAINT fk_article_auteur
        FOREIGN KEY (identifiant_compte_auteur)
        REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_article_statut
        FOREIGN KEY (identifiant_statut_publication)
        REFERENCES statut_publication (identifiant_statut_publication)
);

CREATE INDEX idx_article_statut ON article (identifiant_statut_publication);
CREATE INDEX idx_article_soumis_le ON article (soumis_le);

CREATE TABLE revision_article (
    identifiant_revision_article NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_article NUMBER NOT NULL,
    identifiant_compte_relecteur NUMBER NOT NULL,
    identifiant_type_decision_revision NUMBER NOT NULL,
    commentaire_revision CLOB NULL,
    decide_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_revision_article_article
        FOREIGN KEY (identifiant_article)
        REFERENCES article (identifiant_article),
    CONSTRAINT fk_revision_article_relecteur
        FOREIGN KEY (identifiant_compte_relecteur)
        REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_revision_article_decision
        FOREIGN KEY (identifiant_type_decision_revision)
        REFERENCES type_decision_revision (identifiant_type_decision_revision)
);

