-- Mise a jour automatique du montant paye sur l'adhesion
CREATE OR REPLACE TRIGGER trg_paiement_adhesion_aiud
AFTER INSERT OR UPDATE OR DELETE ON paiement_adhesion
FOR EACH ROW
BEGIN
    UPDATE adhesion_membre am
       SET montant_paye = (
            SELECT COALESCE(SUM(pa.montant), 0)
              FROM paiement_adhesion pa
             WHERE pa.identifiant_adhesion_membre = am.identifiant_adhesion_membre
               AND pa.statut_paiement IN ('paye', 'valide')
       ),
           mis_a_jour_le = SYSTIMESTAMP
     WHERE am.identifiant_adhesion_membre = COALESCE(:NEW.identifiant_adhesion_membre, :OLD.identifiant_adhesion_membre);
END;
