);

CREATE TABLE mode_stockage_media (
    identifiant_mode_stockage_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_mode_stockage VARCHAR2(30 CHAR) NOT NULL,
    libelle_mode_stockage VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_mode_stockage_media_code UNIQUE (code_mode_stockage)
);

CREATE TABLE categorie_produit (
    identifiant_categorie_produit NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_categorie VARCHAR2(30 CHAR) NOT NULL,
    libelle_categorie VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_categorie_produit_code UNIQUE (code_categorie)
);

CREATE TABLE statut_produit (
    identifiant_statut_produit NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_statut VARCHAR2(30 CHAR) NOT NULL,
    libelle_statut VARCHAR2(100 CHAR) NOT NULL,
