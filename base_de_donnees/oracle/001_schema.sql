-- Oracle schema for the chess association website.
-- The model is intentionally normalized close to BCNF:
-- 1. lookups are isolated in dedicated tables
-- 2. account/profile/content/media/order concerns are separated
-- 3. media binaries and external storage references are separated

CREATE TABLE role_compte (
    identifiant_role_compte NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_role VARCHAR2(30 CHAR) NOT NULL,
    libelle_role VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_role_compte_code UNIQUE (code_role),
    CONSTRAINT uq_role_compte_label UNIQUE (libelle_role)
);

CREATE TABLE statut_compte (
    identifiant_statut_compte NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_statut VARCHAR2(30 CHAR) NOT NULL,
    libelle_statut VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_statut_compte_code UNIQUE (code_statut)
);

CREATE TABLE statut_publication (
    identifiant_statut_publication NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_statut VARCHAR2(30 CHAR) NOT NULL,
    libelle_statut VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_statut_publication_code UNIQUE (code_statut)
);

CREATE TABLE type_decision_revision (
    identifiant_type_decision_revision NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_decision VARCHAR2(30 CHAR) NOT NULL,
    libelle_decision VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_type_decision_revision_code UNIQUE (code_decision)
);

CREATE TABLE type_consentement (
    identifiant_type_consentement NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_consentement VARCHAR2(50 CHAR) NOT NULL,
    libelle_consentement VARCHAR2(150 CHAR) NOT NULL,
    base_legale VARCHAR2(150 CHAR) NOT NULL,
    duree_conservation_mois NUMBER(4) NOT NULL,
    est_obligatoire CHAR(1 CHAR) DEFAULT 'Y' NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_type_consentement_code UNIQUE (code_consentement),
    CONSTRAINT ck_type_consentement_required CHECK (est_obligatoire IN ('Y', 'N'))
);

CREATE TABLE type_media (
    identifiant_type_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_type_media VARCHAR2(30 CHAR) NOT NULL,
    libelle_type_media VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_code_type_media UNIQUE (code_type_media)
);

CREATE TABLE type_usage_media (
    identifiant_type_usage_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_usage VARCHAR2(40 CHAR) NOT NULL,
    libelle_usage VARCHAR2(120 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_type_usage_media_code UNIQUE (code_usage)
);

CREATE TABLE statut_droits_media (
    identifiant_statut_droits_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_statut VARCHAR2(30 CHAR) NOT NULL,
