    CONSTRAINT fk_element_album_ressource_media FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media)
);

CREATE TABLE media_article (
    identifiant_media_article NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_article NUMBER NOT NULL,
    identifiant_ressource_media NUMBER NOT NULL,
    identifiant_type_usage_media NUMBER NOT NULL,
    ordre_affichage NUMBER(6) DEFAULT 1 NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_media_article UNIQUE (identifiant_article, identifiant_ressource_media, identifiant_type_usage_media),
    CONSTRAINT fk_media_article_article FOREIGN KEY (identifiant_article) REFERENCES article (identifiant_article),
    CONSTRAINT fk_media_article_asset FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media),
    CONSTRAINT fk_media_article_usage FOREIGN KEY (identifiant_type_usage_media) REFERENCES type_usage_media (identifiant_type_usage_media)
);

CREATE TABLE produit (
    identifiant_produit NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_categorie_produit NUMBER NOT NULL,
    identifiant_statut_produit NUMBER NOT NULL,
    titre VARCHAR2(200 CHAR) NOT NULL,
    slug VARCHAR2(220 CHAR) NOT NULL,
    reference_stock VARCHAR2(80 CHAR) NULL,
    description_courte VARCHAR2(500 CHAR) NOT NULL,
    description_longue CLOB NULL,
    quantite_stock NUMBER(10) DEFAULT 0 NOT NULL,
    disponible_a_partir_de TIMESTAMP WITH TIME ZONE NULL,
    disponible_jusqua TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_produit_slug UNIQUE (slug),
    CONSTRAINT uq_produit_reference_stock UNIQUE (reference_stock),
    CONSTRAINT fk_categorie_produit FOREIGN KEY (identifiant_categorie_produit) REFERENCES categorie_produit (identifiant_categorie_produit),
    CONSTRAINT fk_statut_produit FOREIGN KEY (identifiant_statut_produit) REFERENCES statut_produit (identifiant_statut_produit)
);

CREATE TABLE prix_produit (
    identifiant_prix_produit NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_produit NUMBER NOT NULL,
    code_devise CHAR(3 CHAR) NOT NULL,
    montant NUMBER(10,2) NOT NULL,
    applicable_a_partir_de TIMESTAMP WITH TIME ZONE NOT NULL,
    applicable_jusqua TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_prix_produit UNIQUE (identifiant_produit, code_devise, applicable_a_partir_de),
    CONSTRAINT ck_prix_produit_montant CHECK (montant >= 0),
    CONSTRAINT fk_prix_produit_produit FOREIGN KEY (identifiant_produit) REFERENCES produit (identifiant_produit)
);

CREATE TABLE media_produit (
    identifiant_media_produit NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_produit NUMBER NOT NULL,
    identifiant_ressource_media NUMBER NOT NULL,
    identifiant_type_usage_media NUMBER NOT NULL,
    ordre_affichage NUMBER(6) DEFAULT 1 NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_media_produit UNIQUE (identifiant_produit, identifiant_ressource_media, identifiant_type_usage_media),
    CONSTRAINT fk_media_produit_produit FOREIGN KEY (identifiant_produit) REFERENCES produit (identifiant_produit),
    CONSTRAINT fk_produit_ressource_media FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media),
    CONSTRAINT fk_media_produit_usage FOREIGN KEY (identifiant_type_usage_media) REFERENCES type_usage_media (identifiant_type_usage_media)
);

CREATE TABLE commande_client (
    identifiant_commande_client NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NULL,
    identifiant_statut_commande NUMBER NOT NULL,
    reference_commande VARCHAR2(50 CHAR) NOT NULL,
    nom_facturation VARCHAR2(200 CHAR) NOT NULL,
    courriel_facturation VARCHAR2(320 CHAR) NOT NULL,
    code_devise CHAR(3 CHAR) NOT NULL,
    commandee_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    payee_le TIMESTAMP WITH TIME ZONE NULL,
    annulee_le TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_commande_client_reference UNIQUE (reference_commande),
    CONSTRAINT fk_commande_client_account FOREIGN KEY (identifiant_compte_membre) REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_commande_client_status FOREIGN KEY (identifiant_statut_commande) REFERENCES statut_commande (identifiant_statut_commande)
);

CREATE TABLE ligne_commande_client (
    identifiant_ligne_commande_client NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_commande_client NUMBER NOT NULL,
    identifiant_produit NUMBER NOT NULL,
    quantite NUMBER(10) NOT NULL,
    prix_unitaire NUMBER(10,2) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT ck_ligne_commande_client_quantite CHECK (quantite > 0),
    CONSTRAINT ck_ligne_commande_client_prix_unitaire CHECK (prix_unitaire >= 0),
    CONSTRAINT fk_ligne_commande_client_order FOREIGN KEY (identifiant_commande_client) REFERENCES commande_client (identifiant_commande_client),
    CONSTRAINT fk_ligne_commande_client_produit FOREIGN KEY (identifiant_produit) REFERENCES produit (identifiant_produit)
);



