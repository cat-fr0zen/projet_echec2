    CONSTRAINT uq_statut_produit_code UNIQUE (code_statut)
);

CREATE TABLE statut_commande (
    identifiant_statut_commande NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    code_statut VARCHAR2(30 CHAR) NOT NULL,
    libelle_statut VARCHAR2(100 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_statut_commande_code UNIQUE (code_statut)
);

CREATE TABLE compte_membre (
    identifiant_compte_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    adresse_courriel VARCHAR2(320 CHAR) NOT NULL,
    mot_de_passe_hache VARCHAR2(255 CHAR) NOT NULL,
    courriel_verifie_le TIMESTAMP WITH TIME ZONE NULL,
    identifiant_role_compte NUMBER NOT NULL,
    identifiant_statut_compte NUMBER NOT NULL,
    derniere_connexion_le TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_compte_membre_role FOREIGN KEY (identifiant_role_compte) REFERENCES role_compte (identifiant_role_compte),
    CONSTRAINT fk_compte_membre_status FOREIGN KEY (identifiant_statut_compte) REFERENCES statut_compte (identifiant_statut_compte)
);

CREATE UNIQUE INDEX uq_compte_membre_email_lower
    ON compte_membre (LOWER(adresse_courriel));

CREATE TABLE profil_membre (
    identifiant_profil_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NOT NULL,
    prenom VARCHAR2(100 CHAR) NOT NULL,
    nom VARCHAR2(100 CHAR) NOT NULL,
    date_naissance DATE NULL,
    biographie CLOB NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_profil_membre_account UNIQUE (identifiant_compte_membre),
    CONSTRAINT fk_profil_membre_account FOREIGN KEY (identifiant_compte_membre) REFERENCES compte_membre (identifiant_compte_membre)
);

CREATE TABLE consentement_cookie_visiteur (
    identifiant_consentement_cookie_visiteur NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    jeton_consentement VARCHAR2(128 CHAR) NOT NULL,
    version_consentement VARCHAR2(40 CHAR) NOT NULL,
    accepte_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    choix_theme VARCHAR2(10 CHAR) NULL,
    adresse_ip VARCHAR2(64 CHAR) NULL,
    agent_utilisateur VARCHAR2(400 CHAR) NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_consentement_cookie_visiteur_token UNIQUE (jeton_consentement)
);

CREATE TABLE consentement_membre (
    identifiant_consentement_membre NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_membre NUMBER NOT NULL,
    identifiant_type_consentement NUMBER NOT NULL,
    version_consentement VARCHAR2(40 CHAR) NOT NULL,
    accepte_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    adresse_ip VARCHAR2(64 CHAR) NULL,
    agent_utilisateur VARCHAR2(400 CHAR) NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_consentement_membre UNIQUE (identifiant_compte_membre, identifiant_type_consentement, version_consentement),
    CONSTRAINT fk_consentement_membre_account FOREIGN KEY (identifiant_compte_membre) REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_consentement_membre_type FOREIGN KEY (identifiant_type_consentement) REFERENCES type_consentement (identifiant_type_consentement)
);

CREATE TABLE article (
    identifiant_article NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_auteur NUMBER NOT NULL,
    identifiant_statut_publication NUMBER NOT NULL,
    titre VARCHAR2(200 CHAR) NOT NULL,
    slug VARCHAR2(220 CHAR) NOT NULL,
    resume VARCHAR2(500 CHAR) NOT NULL,
    contenu CLOB NOT NULL,
    soumis_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    publie_le TIMESTAMP WITH TIME ZONE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_article_slug UNIQUE (slug),
    CONSTRAINT fk_article_author FOREIGN KEY (identifiant_compte_auteur) REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_article_status FOREIGN KEY (identifiant_statut_publication) REFERENCES statut_publication (identifiant_statut_publication)
);

CREATE TABLE revision_article (
    identifiant_revision_article NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_article NUMBER NOT NULL,
    identifiant_compte_relecteur NUMBER NOT NULL,
    identifiant_type_decision_revision NUMBER NOT NULL,
    commentaire_revision CLOB NULL,
    decide_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_revision_article_article FOREIGN KEY (identifiant_article) REFERENCES article (identifiant_article),
    CONSTRAINT fk_revision_article_reviewer FOREIGN KEY (identifiant_compte_relecteur) REFERENCES compte_membre (identifiant_compte_membre),
    CONSTRAINT fk_revision_article_decision FOREIGN KEY (identifiant_type_decision_revision) REFERENCES type_decision_revision (identifiant_type_decision_revision)
);

CREATE TABLE ressource_media (
    identifiant_ressource_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_type_media NUMBER NOT NULL,
    identifiant_mode_stockage_media NUMBER NOT NULL,
    identifiant_compte_depot NUMBER NULL,
    nom_fichier_original VARCHAR2(255 CHAR) NOT NULL,
    type_mime VARCHAR2(120 CHAR) NOT NULL,
    taille_octets NUMBER(19) NOT NULL,
    largeur_px NUMBER(10) NULL,
    hauteur_px NUMBER(10) NULL,
    duree_secondes NUMBER(10,3) NULL,
    texte_alternatif VARCHAR2(300 CHAR) NULL,
    date_capture DATE NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_ressource_type_media FOREIGN KEY (identifiant_type_media) REFERENCES type_media (identifiant_type_media),
    CONSTRAINT fk_ressource_media_storage_mode FOREIGN KEY (identifiant_mode_stockage_media) REFERENCES mode_stockage_media (identifiant_mode_stockage_media),
    CONSTRAINT fk_ressource_media_uploader FOREIGN KEY (identifiant_compte_depot) REFERENCES compte_membre (identifiant_compte_membre)
);

CREATE TABLE charge_binaire_media (
    identifiant_charge_binaire_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_ressource_media NUMBER NOT NULL,
    charge_binaire BLOB NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_charge_binaire_ressource_media UNIQUE (identifiant_ressource_media),
    CONSTRAINT fk_charge_binaire_ressource_media FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media)
);

CREATE TABLE reference_externe_media (
    identifiant_reference_externe_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_ressource_media NUMBER NOT NULL,
    fournisseur_stockage VARCHAR2(60 CHAR) NOT NULL,
    uri_stockage VARCHAR2(1000 CHAR) NOT NULL,
    empreinte_sha256 CHAR(64 CHAR) NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT uq_reference_externe_ressource_media UNIQUE (identifiant_ressource_media),
    CONSTRAINT uq_reference_externe_media_uri UNIQUE (uri_stockage),
    CONSTRAINT uq_reference_externe_media_checksum UNIQUE (empreinte_sha256),
    CONSTRAINT fk_reference_externe_ressource_media FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media)
);

CREATE TABLE autorisation_droits_media (
    identifiant_autorisation_droits_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_ressource_media NUMBER NOT NULL,
    identifiant_statut_droits_media NUMBER NOT NULL,
