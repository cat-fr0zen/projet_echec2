);

CREATE TABLE album_media (
    identifiant_album_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_compte_createur NUMBER NOT NULL,
    titre VARCHAR2(200 CHAR) NOT NULL,
    description CLOB NULL,
    est_public CHAR(1 CHAR) DEFAULT 'N' NOT NULL,
    cree_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT ck_album_media_est_public CHECK (est_public IN ('Y', 'N')),
    CONSTRAINT fk_album_media_creator FOREIGN KEY (identifiant_compte_createur) REFERENCES compte_membre (identifiant_compte_membre)
);

CREATE TABLE element_album_media (
    identifiant_element_album_media NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    identifiant_album_media NUMBER NOT NULL,
    identifiant_ressource_media NUMBER NOT NULL,
    ordre_affichage NUMBER(6) DEFAULT 1 NOT NULL,
