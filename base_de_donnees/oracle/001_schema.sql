    mis_a_jour_le TIMESTAMP WITH TIME ZONE DEFAULT SYSTIMESTAMP NOT NULL,
    CONSTRAINT fk_autorisation_droits_ressource_media FOREIGN KEY (identifiant_ressource_media) REFERENCES ressource_media (identifiant_ressource_media),
